    public AllOrders(User user, Order order) {
        initComponents();
        
        loadedBasket = order;
        loadedUser = user;
        OrderManager oManager = new OrderManager();
        
        HashMap<Integer,Delivery> deliveries = oManager.LoadDeliveries();
        
        DefaultTableModel basketTableModel = (DefaultTableModel) tblAllOrders.getModel();
        
        
        for(Map.Entry<Integer, Delivery> entry : deliveries.entrySet())
        {
            String summary = "";
            
            //loops through orderlines
            for (Map.Entry<Integer, OrderLine> oL : entry.getValue().getOrder().getOrderLines().entrySet()) 
            {
                Product product = oL.getValue().getProduct();
                
                summary += oL.getValue().getQuantity() + " X " + product.getName() + "\n" ;
            }
            
            Delivery actualDelivery = entry.getValue();
            
            Address address = actualDelivery.getAddress();
            
            String addressString = address.getStreet() + "\n" + address.getTown() + "\n" + address.getCity() + "\n" + address.getCountry() + "\n" + address.getPostCode();
            
            
            Order orderInDelivery = actualDelivery.getOrder();
            //adds rows to table model
            basketTableModel.addRow(new Object[]{
               orderInDelivery.getOrderId(),
                "Â£" + orderInDelivery.getPrice(),
                actualDelivery.getStatus().toString(),
                "Delivery",
                actualDelivery.getPredictedDeliveryDate(),
                summary,
                actualDelivery.getType().toString(),
                addressString
                
                
            });
            
            
            
            
            
        }
        
        tblAllOrders.setModel(basketTableModel);
    }

