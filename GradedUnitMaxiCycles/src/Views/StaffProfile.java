    private void btnConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmActionPerformed
        // TODO add your handling code here:
        
        // TODO add your handling code here:
        
        UserManager uManager = new UserManager();

        try
        {
            //Customer(String userName, String password, String firstName, String surname, String emailAddress, String phoneNumber, Address address, int numberOfFailedLoginAttempts, LocalDate dateOfAccountLock, LocalDate dateRegistered, String preferredPaymentMethod, String paymentDetails, Address preferredDeliveryAddress, String companyName)
            String userName = txtUsername.getText();
            String password = txtPassword.getText();
            String firstName = txtFirstName.getText();
            String surname = txtLastName.getText();
            String emailAddress = txtEmail.getText();
            String phoneNumber = txtPhoneNumber.getText();
        
            String street = txtStreet.getText();
            String town = txtTown.getText();
            String city = txtCity.getText();
            String country = txtCountry.getText();
            String postCode = txtPostCode.getText();
            
            
            
            //check if all fields are not empty
            if(userName.equals("") || password.equals("")  || firstName.equals("") || surname.equals("") || emailAddress.equals("") || phoneNumber.equals("") || street.equals("") || town.equals("") || city.equals("") || country.equals("") || postCode.equals("")  )
            {
                //display complete all fields error message
                JOptionPane.showMessageDialog(rootPane, "Please Complete All Fields");
            }
            else
            {
                //creates hashmap of users
                //loads users into hashmap
               HashMap<String, User> users = uManager.LoadUsers();
               
              
               
               
               //creates new address
               //public Address(String street, String town, String city, String country, String postCode, int addressId)
               Address address = new Address(street, town, city, country, postCode, loadedStaff.getAddress().getAddressId());
               
               //creates new customer
               //public Staff(Date dateEmployed, String password, String firstName, String surname, String emailAddress, String phoneNumber, Address address, int numberOfFailedLoginAttempts, Date dateOfAccountLock, Date dateRegistered, List<Integer> roleIds))
               Staff staff = new Staff(loadedStaff.getDateEmployed(),userName, password, firstName, surname,emailAddress,phoneNumber, address, loadedStaff.getNumberOfFailedLoginAttempts(),loadedStaff.getDateOfAccountLock(), loadedStaff.getDateRegistered(), loadedStaff.getRole()  );
               

               
               //checks if address exists in the database
               
               if(!uManager.IsAddressRegistered(address))
               {
                   address = uManager.RegisterAddress(address);
                   staff.setAddress(address);
               }
               else
               {
                   staff.setAddress(uManager.GetRegisteredAddress(address));
               }

               
               
                
            
               //Updates customer
               uManager.UpdateUser(staff);
               
               if(staff == null)
               {
                   JOptionPane.showMessageDialog(rootPane, "Error Update Unsuccessful");
               }
               else
               {
                   JOptionPane.showMessageDialog(rootPane, "Information Updated Successfully ");
                   FillStaffInsformation(staff);
               }
               

               
               
               
               
               
               
               
            }
        
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Valid Information");
        }
    }//GEN-LAST:event_btnConfirmActionPerformed

