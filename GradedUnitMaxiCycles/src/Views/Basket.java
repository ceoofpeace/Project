    public Basket(User user, Order order, String lastFrame, Product product) {
        initComponents();
        loadedUser = user;
        previousFrame = lastFrame;
        loadedProduct = product;
        if(order == null){
            loadedBasket = new Order();
        }
        else{
            loadedBasket = order;
        }
        
        DefaultTableModel basketTableModel = (DefaultTableModel) tblBasket.getModel();
        for(Map.Entry<Integer, OrderLine> entry : loadedBasket.getOrderLines().entrySet())
        {
            
            System.out.println(loadedBasket.getOrderLines().size());
            OrderLine actualOrderLine = entry.getValue();
            Product ProductInOrderLine = actualOrderLine.getProduct();
            //adds rows to basket table model
            basketTableModel.addRow(new Object[]{
               ProductInOrderLine.getProductId(),
                ProductInOrderLine.getName(),
                "£" + ProductInOrderLine.getPrice(),
                actualOrderLine.getQuantity()
            });
            
            
            
            
            
        }
        
        tblBasket.setModel(basketTableModel);
        
        lblOrderTotal.setText("Total: £" + String.valueOf(loadedBasket.CalculateOrderTotal()));
    }

