    private void btnBack1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBack1ActionPerformed
        // TODO add your handling code here:
        
        JFrame frame;
        switch (previousFrame) {
            case "ProductDetails":
                frame = new ProductDetails(loadedUser, loadedProduct, loadedBasket, new ArrayList<>(), "");
                
                break;
            case "Products":
                frame = new Products(loadedUser,  loadedBasket, new ArrayList<>(), "");
                
                break;
            case "HomePage":
                frame = new HomePage(loadedUser, loadedBasket);
                
                break;
            
            default:
                throw new AssertionError();
        }
        frame.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_btnBack1ActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        // TODO add your handling code here:
        loadedBasket = new Order();
        DefaultTableModel basketTableModel = (DefaultTableModel) tblBasket.getModel();
        basketTableModel.setRowCount(0);
        
        tblBasket.setModel(basketTableModel);
    }//GEN-LAST:event_btnClearActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        
        int row = tblBasket.getSelectedRow();
        
        if(row == -1)
        {
            JOptionPane.showMessageDialog(rootPane, "No Item Selected");
        }
        
        else
        {
            DefaultTableModel basketTableModel = (DefaultTableModel) tblBasket.getModel();
            loadedBasket.removeOrderLine((int) tblBasket.getValueAt(row, 0));
            basketTableModel.removeRow(row);
            tblBasket.setModel(basketTableModel);
        }
        
        
    }//GEN-LAST:event_btnRemoveActionPerformed

