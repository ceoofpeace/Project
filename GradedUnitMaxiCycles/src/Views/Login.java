    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        UserManager uManager = new UserManager();
        String username = lblUsername.getText();
        String password = String.valueOf(txtPsWrd.getPassword()) ;
        HashMap<String,User> users = uManager.LoadUsers();
        
        if(username.equals("") || password.equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Please Fill All FIelds");
        }
        else if(!users.containsKey(username))
        {
            JOptionPane.showMessageDialog(rootPane, "No Existing Users With Input Username");
        }
        else
        {
            User user = uManager.logIn(username, password);
        
            if(user == null){
                JOptionPane.showMessageDialog(rootPane, "Incorrect Password");
            }
            else
            {
                lblConfirmation.setText("hello " + user.getFirstName());
                HomePage homePage = new HomePage(user, null);
                homePage.setVisible(true);
                this.dispose();
            }
        }

        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

    private void btnForgotPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnForgotPasswordActionPerformed
        // TODO add your handling code here:
        
        
        Random random = new Random();
        int code = random.nextInt(10000);
        UserManager uManager = new UserManager();
        String email = JOptionPane.showInputDialog("Please Enter Your Email");
        
        HashMap<String, User> users = uManager.LoadUsers();
        
        User userToChangePassword = null;
        for (Map.Entry<String, User> entry : users.entrySet()) {
            if(entry.getValue().getEmailAddress().equals(email)){
                userToChangePassword = entry.getValue();
            }
            
        }
        
        if(userToChangePassword == null)
        {
            JOptionPane.showMessageDialog(rootPane, "this email is not recognized");
            return;
        }
        
        MessageManager mManager = new MessageManager();
        
        mManager.sendEmail(email, "MaxiCycles Password Reset Code", "here is your password reset code: " + code);
        
        
        
        try{
            
            int inputCode = Integer.parseInt(  JOptionPane.showInputDialog("a code has been emailed to you, Please Enter Code"));
            
            if(inputCode == code)
            {
                String newPassword = JOptionPane.showInputDialog("Please Enter Your New Password");
                
                uManager.ChangeUserPassword(userToChangePassword, newPassword);
                JOptionPane.showMessageDialog(rootPane, "your password has been changed");
                HomePage homePage = new HomePage();
                homePage.setVisible(true);
                this.dispose();
            }
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(rootPane, "Code Failed Please Try Again");
            System.out.println(""+ex);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_btnForgotPasswordActionPerformed


