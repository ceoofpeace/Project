    private void btnAddToBasketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToBasketActionPerformed
        
        
// TODO add your handling code here:
        try
        {
            String quantity = txtQuantity.getText();
            if(loadedUser == null)
            {
                Login login = new Login();
                login.setVisible(true);
                this.dispose();
            }
            else if(quantity.equals("") )
            {
                JOptionPane.showMessageDialog(rootPane, "Please Enter A Quantity");
            }
            else if(Integer.parseInt(quantity) > loadedProduct.getQuantity())
            {
                JOptionPane.showMessageDialog(rootPane, "Product Not Available In Selected Quantity");
            }
            else if(Integer.parseInt(quantity) <= 0)
            {
                JOptionPane.showMessageDialog(rootPane, "Please Enter A Valid Quantity");
            }
            else
            {
                if(loadedBasket == null)
                {
                    loadedBasket = new Order();
                }
                
                OrderLine oL = loadedBasket.ExistsInOrder(loadedProduct);
                if(oL == null)
                {
                    //public OrderLine( int quantity, Order order)
                    oL = new OrderLine(Integer.parseInt(quantity), loadedBasket.getOrderId(), loadedProduct);
                    loadedBasket.addOrderLine(oL);
                }
                else if(oL.getQuantity() + Integer.parseInt(quantity) > loadedProduct.getQuantity())
                {
                    JOptionPane.showMessageDialog(rootPane, "Quantity is over product stock");
                    return;
                }
                else
                {
                    oL.setQuantity(oL.getQuantity() + Integer.parseInt(quantity));
                    loadedBasket.UpdateOrderLine(oL);
                }
                
                JOptionPane.showMessageDialog(rootPane, quantity + " x " + loadedProduct.getName() + " Added to Basket");
                Products products = new Products(loadedUser, loadedBasket, loadedTagIds, loadedSearchString);
                products.setVisible(true);
                this.dispose();
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Valid Information");
        }
       
    }//GEN-LAST:event_btnAddToBasketActionPerformed

